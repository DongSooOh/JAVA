package javaTest;

public class BooleanExample {
	public static void main(String[] args) {
		boolean stop = false;
		if (stop) {
			System.out.println("중지합니다.");
		} else {
			System.out.println("시작합니다.");
		}
		
		boolean start = true;
		if (start) {
			System.out.println("중지합니다.");
		} else {
			System.out.println("시작합니다.");
		}
	}
}
