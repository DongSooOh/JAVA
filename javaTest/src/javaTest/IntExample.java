package javaTest;

public class IntExample {
	public static void main(String[] args) {
		int var1 = 10; // 10진수(0~9)
		int var2 = 012; // 8진수(0~7)
		int var3 = 0XA; // 16진수(0~9, A(10)~F(15))
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);

	}

}
