package peopleStudent;

public class ForForExample {

	public static void main(String[] args) {
				
		System.out.println("备备窜 矫累");
		for (int x = 1; x <= 9; x++) {
			
			System.out.println("----------" + x + "窜 ----------");
			
			for (int y = 1; y <= 9; y++) {
			System.out.println(x + " x " + y + "= " + (x * y));
			}	
		}
		System.out.println("备备窜 场");

	}

}
